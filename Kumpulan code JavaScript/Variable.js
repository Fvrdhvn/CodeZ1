/*Apa itu variable? 
    Variable adalah suatu tempat yg digunakan untuk menampung data atau konstanta
     di memori yang mempunyai nilai yg dapat berubah-ubah selama proses program*/

var x = 5;
var y = 6;
var z = x + y;
console.log(z);

// Operator Aritmatika
// Operator aritmatika digunakan untuk melakukan aritmatika pada angka.

// Operator	Penjelasan	Contoh
// +	Penambahan	1+1
// -	Pengurangan	1-1
// *	Pengalian	1*3
// /	Pembagian	10/2
// %	Modulus (sisa pembagian)	7/2
// ++	Increment	variable++
// --	Decrement	variable--
// Contoh :

var a = 10;
var b = 20;
var ab = a + b;

// Operator Penggabungan String
// Operator "+" juga dapat digunakan untuk menggabungkan string.

// Contoh :

var txt1 = "Saya suka";
var txt2 = "Nasi";
var txt3 = "Goreng";
var txt4 = txt1 + " " + txt2 + " " + txt3;
console.log("txt4"); // output : "Saya suka Nasi Goreng"

// Operator Pembanding
// Operator	Penjelasan
// ==	Sama dengan isinya
// ===	Sama isinya dan sama tipenya
// !=	Tidak sama dengan isinya
// !==	Tidak sama isinya dan tidak sama tipenya
// >	Lebih dari
// <	Kurang dari
// >=	Lebih dari atau sama dengan
// <=	Kurang dari atau sama dengan
// ?	Operator ternary (if else sederhana)
// Operator Logika
// Operator	Deskripsi
// &&	Logika And. Bernilai TRUE bila kedua kondisi bernilai TRUE.
// ||	Logika Or. Bernilai TRUE bila salah satu atau kedua kondisi bernilai TRUE.
// !	Logika Not. Logika Pembalik. Bernilai TRUE bila kondisi FALSE, dan bernilai FALSE bila kondisi TRUE.
